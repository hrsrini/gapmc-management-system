function phaseForRow(id, row) {
  const joined = row.join(" ");
  if (id.startsWith("TP-UAT-")) return "uat_business";
  if (id === "TP-M01-010" || id === "TP-M03-006" || id === "TP-M04-004") return "pending_not_in_scope";
  if (id === "TP-RENT-TDS" || id === "TP-USR-MFA" || id === "TP-NFR-OFFLINE") return "pending_not_in_scope";
  if (id.startsWith("TP-OQ-") && (row[3] === "Deferred" || joined.includes("Not built"))) return "pending_not_in_scope";
  if (joined.includes("UAT Deferred")) return "pending_not_in_scope";
  if (joined.includes("Not integrated") || joined.includes("Not implemented")) return "pending_not_in_scope";
  return "implemented_ready_sit";
}

for (let li = 1; li < lines.length; li++) {
  if (!lines[li].trim()) continue;
  const row = parseCSVLine(lines[li]);
  const id = row[0] ?? "";
  const phase = phaseForRow(id, row);
  out.push(rowToLine([...row, phase, "", ""]));
}

