/** Strip RFC4180 quotes so re-encoding does not triple-quote fields. */
function unescapeCSVField(f) {
  const t = String(f);
  if (t.length >= 2 && t.startsWith('"') && t.endsWith('"')) {
    return t.slice(1, -1).replace(/""/g, '"');
  }
  return f;
}

function parseCSVLine(line) {
  const res = [];
  let field = "";
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      inQ = !inQ;
      field += c;
      continue;
    }
    if (c === "," && !inQ) {
      res.push(unescapeCSVField(field));
      field = "";
      continue;
    }
    field += c;
  }
  res.push(unescapeCSVField(field));
  return res;
}

function encodeField(f) {
  const needsQuote = /[",\n\r]/.test(f);
  if (!needsQuote) return f;
  return `"${String(f).replace(/"/g, '""')}"`;
}

function rowToLine(cells) {
  return cells.map(encodeField).join(",");
}

